        <h1>SCOTLAND: A PERPLEXING PLACE …</h1>

        <p>Welcome to the digital archive of the <i>Scottish Government Yearbooks</i>, published by the University of Edinburgh's
            'Unit for the Study of Government in Scotland' between 1976 and 1992. The Unit was set up in the mid-1970s
            to address 'Our Changing Scotland' and the certainty – as it seemed at the time – of a devolved Assembly in
            Edinburgh. The <span class="italic">Yearbooks</span> reflect the spirit of that enterprise: contributions cut
            across academic disciplines … sociology, political science, social policy, history, economics, theology, education, medicine, law.
            And they spanned a wide range of academic institutions, centred upon – but not limited to – Scotland’s universities.
            But the Yearbooks went beyond academia to embrace lawyers, journalists, political activists, policy makers,
            civil servants, public sector managers, local government officers and clerics. Through this rich mix the
            <span class="italic">Yearbooks</span> sought, as the opening volume’s <a
                href="https://www.era.lib.ed.ac.uk/bitstream/1842/8996/1/1976_1_Our%20changing%20Scotland.pdf" target="_blank">editorial</a> made clear, to engage with a Scotland which was proving
            to be "a perplexing place".
        </p>
        <p>
            The <span class="italic">Yearbooks</span> provide unparalleled insights into a crucial period in Scotland's
            political and social development.
            They bear witness to, and carefully analyse, a Scotland in which a devolved Assembly seemed inevitable, a
            Scotland where those assumptions were dashed through the referendum of 1979, and a Scotland which rejected
            Thatcherism but endured its radical shaking of key institutions. The <span class="italic">Yearbooks</span>
            end in 1992, when 'home rule' stood reinvigorated and when the question of devolution was again dominating
            the Scottish political agenda. As the <a href="https://www.era.lib.ed.ac.uk/bitstream/1842/9344/1/1992_1_introduction.pdf" target="_blank">introduction</a>
            to that final volume notes there was by then "a real sense of an uncompleted agenda" in,
            and for, Scotland. To address that agenda the <span class="italic">Yearbooks</span> morphed, in 1992, into
            <a href="http://www.euppublishing.com/journal/scot" target="_blank">Scottish Affairs</a>, Scotland's
            longest running peer-reviewed journal of contemporary Scottish issues. And the Unit developed, briefly, into
            the Governance of Scotland Forum and from there into the <a href="http://www.institute-of-governance.org/" target="_blank">Institute of Governance</a>.
        </p>
        <p>
            In this archive you can find informed and critical articles on a wide range of topics of high relevance both
            to Scotland's past and to its future. In light of the 2014 referendum, the third on Scotland's constitutional
            relationship with/to the United Kingdom, the <a href="http://www.institute-of-governance.org/" target="_blank">Institute of Governance</a>
            felt the time was right to provide the full content of the <span class="italic">Yearbooks</span> online to all.
            Our changing Scotland remains a perplexing place with an uncompleted agenda: here you will find much that helps explain where,
            in our recent history, we have come from.
        </p>
        <p>
            <span class="bold">Michael Rosie</span>, Director, Institute of Governance, July 2014.
        </p>
